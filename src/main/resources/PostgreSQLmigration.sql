-- ==== TABELA: courses ====

DROP TABLE IF EXISTS courses CASCADE;

CREATE TABLE courses (
                         course_id   bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         created_at  timestamp(6)   NOT NULL,
                         description varchar(255)   NOT NULL,
                         title       varchar(255)   NOT NULL,
                         updated_at  timestamp(6)   NOT NULL,
                         title_lvl   title_level_enum NOT NULL
);

-- ==== TABELA: lessons ====

DROP TABLE IF EXISTS lessons CASCADE;

CREATE TABLE lessons (
                         lesson_id      bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         content        varchar(255) NOT NULL,
                         created_at     timestamp(6) NOT NULL,
                         title          varchar(255) NOT NULL,
                         updated_at     timestamp(6) NOT NULL,
                         course_id      bigint       NOT NULL,
                         lesson_number  integer      NOT NULL,
                         required_level integer      NOT NULL,
                         CONSTRAINT fk_lessons_course
                             FOREIGN KEY (course_id) REFERENCES courses(course_id)
);

-- ==== TABELA: assignments ====

DROP TABLE IF EXISTS assignments CASCADE;

CREATE TABLE assignments (
                             assignment_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             created_at    timestamp(6)   NOT NULL,
                             description   text           NOT NULL,
                             title         varchar(255)   NOT NULL,
                             updated_at    timestamp(6)   NOT NULL,
                             lesson_id     bigint         NOT NULL,
                             title_lvl     title_level_enum NOT NULL,
                             CONSTRAINT fk_assignments_lesson
                                 FOREIGN KEY (lesson_id) REFERENCES lessons(lesson_id)
);

-- ==== TABELA: solutions ====

DROP TABLE IF EXISTS solutions CASCADE;

CREATE TABLE solutions (
                           solution_id  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                           content      text         NOT NULL,
                           created_at   timestamp(6) NOT NULL,
                           assignment_id bigint      NOT NULL,
                           language     varchar(255) NOT NULL,
                           CONSTRAINT fk_solutions_assignment
                               FOREIGN KEY (assignment_id) REFERENCES assignments(assignment_id)
);

-- ==== TABELA: comments ====
-- rating jako domena rating_int

DROP TABLE IF EXISTS comments CASCADE;

CREATE TABLE comments (
                          comment_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          content    varchar(255) NOT NULL,
                          created_at timestamp(6) NOT NULL,
                          rating     rating_int,
                          lesson_id  bigint NOT NULL,
                          user_id    bigint NOT NULL,
                          CONSTRAINT fk_comments_lesson
                              FOREIGN KEY (lesson_id) REFERENCES lessons(lesson_id),
                          CONSTRAINT fk_comments_user
                              FOREIGN KEY (user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: daily_task ====

DROP TABLE IF EXISTS daily_task CASCADE;

CREATE TABLE daily_task (
                            task_id     bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            description varchar(255),
                            title       varchar(255)
);

-- ==== TABELA: daily_task_assignment ====

DROP TABLE IF EXISTS daily_task_assignment CASCADE;

CREATE TABLE daily_task_assignment (
                                       id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                       assignment_date    date,
                                       daily_task_task_id bigint,
                                       user_user_id       bigint,
                                       CONSTRAINT fk_daily_task_assignment_task
                                           FOREIGN KEY (daily_task_task_id) REFERENCES daily_task(task_id),
                                       CONSTRAINT fk_daily_task_assignment_user
                                           FOREIGN KEY (user_user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: daily_task_result ====

DROP TABLE IF EXISTS daily_task_result CASCADE;

CREATE TABLE daily_task_result (
                                   daily_task_result_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                   completed_at         timestamp(6),
                                   points               bigint,
                                   task_id              bigint NOT NULL,
                                   user_id              bigint NOT NULL,
                                   CONSTRAINT fk_daily_task_result_task
                                       FOREIGN KEY (task_id) REFERENCES daily_task(task_id),
                                   CONSTRAINT fk_daily_task_result_user
                                       FOREIGN KEY (user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: quiz ====

DROP TABLE IF EXISTS quiz CASCADE;

CREATE TABLE quiz (
                      quiz_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      title   varchar(255)
);

-- ==== TABELA: question ====
-- TU wprowadzamy "obiektowość" przez tablicę możliwych odpowiedzi
-- zamiast osobnej relacji question_options.

DROP TABLE IF EXISTS question CASCADE;

CREATE TABLE question (
                          question_id    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          content        varchar(255) NOT NULL,
                          correct_answer varchar(255) NOT NULL,
                          quiz_id        bigint       NOT NULL,
                          options        varchar(255)[] NOT NULL, -- tablica opcji odpowiedzi
                          CONSTRAINT fk_question_quiz
                              FOREIGN KEY (quiz_id) REFERENCES quiz(quiz_id)
);

-- Stara tabela question_options NIE jest już potrzebna
-- (jej rolę przejęła tablica options w question).
-- Jeśli chcesz zostawić, możesz ją potraktować np. jako typ złożony,
-- ale tutaj ją po prostu usuwamy.

DROP TABLE IF EXISTS question_options CASCADE;

-- ==== TABELA: quiz_result ====

DROP TABLE IF EXISTS quiz_result CASCADE;

CREATE TABLE quiz_result (
                             quiz_result_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             completed_at   timestamp(6),
                             points         bigint,
                             quiz_id        bigint NOT NULL,
                             user_id        bigint NOT NULL,
                             CONSTRAINT fk_quiz_result_quiz
                                 FOREIGN KEY (quiz_id) REFERENCES quiz(quiz_id),
                             CONSTRAINT fk_quiz_result_user
                                 FOREIGN KEY (user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: progress ====

DROP TABLE IF EXISTS progress CASCADE;

CREATE TABLE progress (
                          progress_id  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          completed_at timestamp(6),
                          course_id    bigint NOT NULL,
                          lesson_id    bigint NOT NULL,
                          user_id      bigint NOT NULL,
                          CONSTRAINT fk_progress_course
                              FOREIGN KEY (course_id) REFERENCES courses(course_id),
                          CONSTRAINT fk_progress_lesson
                              FOREIGN KEY (lesson_id) REFERENCES lessons(lesson_id),
                          CONSTRAINT fk_progress_user
                              FOREIGN KEY (user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: submissions ====

DROP TABLE IF EXISTS submissions CASCADE;

CREATE TABLE submissions (
                             submission_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             content       text           NOT NULL,
                             grade         double precision,
                             graded_at     timestamp(6),
                             submitted_at  timestamp(6)   NOT NULL,
                             assignment_id bigint         NOT NULL,
                             user_id       bigint         NOT NULL,
                             CONSTRAINT fk_submissions_assignment
                                 FOREIGN KEY (assignment_id) REFERENCES assignments(assignment_id),
                             CONSTRAINT fk_submissions_user
                                 FOREIGN KEY (user_id) REFERENCES "user"(user_id)
);

-- ==== TABELA: video_metadata ====
-- Zastosowanie typu złożonego video_file

DROP TABLE IF EXISTS video_metadata CASCADE;

CREATE TABLE video_metadata (
                                id   bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                file video_file  -- (file_name, file_path, file_size)
);

