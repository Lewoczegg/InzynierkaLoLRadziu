-- ==== ENUMY ====

CREATE TYPE title_level_enum AS ENUM ('ADVANCED', 'BEGINNER', 'INTERMEDIATE');

-- ==== DOMENY (typy z dodatkowymi ograniczeniami) ====

CREATE DOMAIN email_address AS varchar(255)
    CHECK (VALUE LIKE '%@%' AND POSITION('@' IN VALUE) > 1);

CREATE DOMAIN rating_int AS integer
    CHECK (VALUE BETWEEN 1 AND 5);

-- ==== TYPY ZŁOŻONE (COMPOSITE TYPES) ====

-- Imię + nazwisko jako jeden obiekt
CREATE TYPE full_name AS (
    first_name varchar(255),
    surname    varchar(255)
    );

-- Dane kontaktowe jako jeden obiekt
CREATE TYPE contact_info AS (
    email        email_address,
    phone_number varchar(255)
    );

-- Metadane pliku wideo jako jeden obiekt
CREATE TYPE video_file AS (
    file_name varchar(255),
    file_path varchar(255),
    file_size bigint
    );

-- Poziom użytkownika jako obiekt (liczba + poziom tekstowy)
CREATE TYPE user_level AS (
    level integer,
    title title_level_enum
    );

-- =========================================
--           TABELKI WŁAŚCIWE
-- =========================================

-- ==== TABELA: role ====

DROP TABLE IF EXISTS role CASCADE;

CREATE TABLE role (
                      role_id   bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      role_name varchar(255) NOT NULL UNIQUE
);

-- ==== TABELA: "user" ====
-- Zastosowanie:
--  - full_name        (obiekt)
--  - contact_info     (obiekt)
--  - user_level       (obiekt)

DROP TABLE IF EXISTS "user" CASCADE;

CREATE TABLE "user" (
                        user_id    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        name       full_name      NOT NULL,  -- (first_name, surname)
                        contacts   contact_info   NOT NULL,  -- (email, phone_number)
                        age        integer,
                        created_at timestamp(6)   NOT NULL,
                        updated_at timestamp(6)   NOT NULL,
                        password   varchar(255)   NOT NULL,
                        role_id    bigint         NOT NULL,
                        level_info user_level     NOT NULL,  -- (level, title)
                        username   varchar(255)   NOT NULL UNIQUE,

                        CONSTRAINT fk_user_role
                            FOREIGN KEY (role_id) REFERENCES role(role_id)
);

